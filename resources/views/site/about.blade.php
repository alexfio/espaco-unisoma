          <div class="range range-30 text-left">
            <div class="cell-sm-6">
              <h5 class="question">How does therapy work?</h5>
              <p class="answer">Both individual psychotherapy and couples therapy give you the opportunity to transform your life  by looking at how you are living, thinking, and interacting; and then bringing your behavior in line with your highest aspirations for yourself.</p>
              <h5 class="question">How do I know if I need counseling?</h5>
              <p class="answer">If you are struggling in your life or in your relationship, you could likely benefit from individual or couples therapy. People who come to see me often are tired of going around in circles, sometimes don’t respect how they are living their lives, and want to find solutions for truly getting unstuck.</p>
              <h5 class="question">How frequently do you work with clients?</h5>
              <p class="answer">I usually work with individuals once or twice per week, depending on the specifics of their lives and the issues they are working on. I usually work with couples once a week at first, and then biweekly as they move toward a better relationship.</p>
            </div>
            <div class="cell-sm-6">
              <h5 class="question">Where is your office located?</h5>
              <p class="answer">My office is located on Connecticut Avenue in Washington DC, directly across from the National Zoo in the Woodley Park/Cleveland Park neighborhood. My office is a short walk from the Woodley Park and Cleveland Park stops on the Red Line of the Washington DC Metro.</p>
              <h5 class="question">What happens in a therapy session?</h5>
              <p class="answer">Therapy sessions will begin by going over how successful you were since the previous session in hitting your targets and what difficulties you had.  We will then set an agenda, so we can efficiently use our time.  We will be sure to take stock of why things went well, so it is clear how to ensure things can continue to go well.  We will also utilize interventions to eliminate the existing problems by carefully analyzing them, and develop existing or new skills in order to better manage them in the coming week.</p><a class="button button-primary" href="faq.html">More FAQ</a>
